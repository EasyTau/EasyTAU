public class TauEvent {

    private String event;
    private String date;
    private String hebrew_date;

    public String getEvent() {
        return event;
    }

    public void setEvent(String event) {
        this.event = event;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getHebrewDate() {
        return hebrew_date;
    }

    public void setHebrewDate(String hebrew_date) {
        this.hebrew_date = hebrew_date;
    }

}
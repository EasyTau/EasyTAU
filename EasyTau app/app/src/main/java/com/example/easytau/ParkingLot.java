public class ParkingLot {
    private String name;
    private String address;
    private String price_day;
    private String price_night;
    private String price_allday;
    private String note;
    private String status;
    private String status_time;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPrice_day() {
        return price_day;
    }

    public void setPrice_day(String price_day) {
        this.price_day = price_day;
    }

    public String getPrice_night() {
        return price_night;
    }

    public void setPrice_night(String price_night) {
        this.price_night = price_night;
    }

    public String getPrice_allday() {
        return price_allday;
    }

    public void setPrice_allday(String price_allday) {
        this.price_allday = price_allday;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getStatus_time() {
        return status_time;
    }

    public void setStatus_time(String status_time) {
        this.status_time = status_time;
    }
}
